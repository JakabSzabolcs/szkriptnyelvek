#!/usr/bin/env python3

# a set egy halmaz, amiben csak egyedi elemek lehetnek
# a set() függvény létrehoz egy üres halmazt

def main():
    kosar = ['alma', 'körte', 'alma', 'szilva', 'körte']
    kosar = set(kosar)
    print(kosar)
    # kardinalitás (hány elem van a halmazban)


if __name__ == "__main__":
    main()
